    public boolean validar_obrigatorios_rurais() {
        String s = ((JTextField) TEMPOCOMPRA.getDateEditor().getUiComponent()).getText();
        erro = 0;
        if ("".equals(NOMEFAZENDA.getText()) && erro == 0) {
            JOptionPane.showMessageDialog(null, "Informe o nome da propriedade rural", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            NOMEFAZENDA.requestFocus();
            erro = 1;
        } else if ("".equals(LOGRADOURO.getText()) && erro == 0) {
            JOptionPane.showMessageDialog(null, "Informe o logradouro da propriedade rural", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            LOGRADOURO.requestFocus();
            erro = 1;
        } else if ("".equals(MUNICEDE.getText()) && erro == 0) {
            JOptionPane.showMessageDialog(null, "Informe o municipio sede da propriedade rural", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            MUNICEDE.requestFocus();
            erro = 1;
        } else if ("".equals(AREAFAZENDA.getText()) && erro == 0) {
            JOptionPane.showMessageDialog(null, "Informe a área da propriedade rural", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            AREAFAZENDA.requestFocus();
            erro = 1;
        } else if ("".equals(s) && erro == 0) {
            JOptionPane.showMessageDialog(null, "Informe a data de aquisição da propriedade rural", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            TEMPOCOMPRA.requestFocus();
            erro = 1;
        } else {
            String data = Util_Controller.verificar_Data(df.format(TEMPOCOMPRA.getDate()), false);
            if ("//".equals(data)) {
                JOptionPane.showMessageDialog(null, "A data informada é inválida", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                erro = 1;
                TEMPOCOMPRA.setDate(null);
                TEMPOCOMPRA.requestFocus();
            }
        }

        if (!"   .   .   .   - ".equals(CODINCRA.getText()) && erro == 0) {
            String codI = si.validaCodIncra(CODINCRA.getText());
            if ("".equals(codI)) {
                erro = 1;
                JOptionPane.showMessageDialog(null, "O código no INCRA da propriedade rural informada é inválido.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                CODINCRA.setValue("");
                CODINCRA.requestFocus();
            } else {
                String i;
                i = si.verificar_INCRA(codI, id_sindicalizado);

                if ("tem dono".equals(i)) {
                    erro = 1;
                    JOptionPane.showMessageDialog(null, "O código no INCRA da propriedade rural informada já está cadastrado no sistema.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                    CODINCRA.setValue("");
                    CODINCRA.requestFocus();
                }

            }
        }

        if (!" .   .   - ".equals(Numero_NIRF.getText()) && erro == 0) {
            String nirf = si.validarNIRF(Numero_NIRF.getText());
            if ("".equals(nirf)) {
                erro = 1;
                JOptionPane.showMessageDialog(null, "O número do NIRF é inválido.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                Numero_NIRF.setValue("");
                Numero_NIRF.requestFocus();
            } else {
                String i;
                if ("alterar".equals(status)) {
                    i = si.verificar_NIRF(nirf, id_sindicalizado);
                } else {
                    i = si.verificar_NIRF(nirf, 0);
                }
                if ("tem dono".equals(i)) {
                    erro = 1;
                    JOptionPane.showMessageDialog(null, "O número do NIRF informado já está cadastrado", "Atenção RG inválido", JOptionPane.INFORMATION_MESSAGE);
                    Numero_NIRF.setValue("");
                    Numero_NIRF.requestFocus();
                }
            }
        }

        if (erro == 0) {
            CONT = true;
        } else if (erro == 1) {
            CONT = false;
        }

        return CONT;
    }

    public void LISTAR_TABELA_Rural(ArrayList<Propriedades_Rurais> DADOS_RU) {
        DefaultTableModel dtma = (DefaultTableModel) TABELA_PROPRIEDADE_RURAL.getModel();
        dtma.setNumRows(0);

        TABELA_PROPRIEDADE_RURAL.getColumnModel().getColumn(0).setMinWidth(0); // OCULTA A COLUNA (ID) DA TABELA PARA NÃO APARECER PARA O USUARIO
        TABELA_PROPRIEDADE_RURAL.getColumnModel().getColumn(0).setMaxWidth(0); // OCULTA A COLUNA (ID) DA TABELA PARA NÃO APARECER PARA O USUARIO
        TABELA_PROPRIEDADE_RURAL.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);

        DADOS_RU.forEach((dr) -> {
            String ID_pro = "";
            if (dr.getId_proprie() > 0) {
                ID_pro = String.valueOf(dr.getId_proprie());
            }
            String de = Util_Controller.DATE_STRING(dr.getDataCompra());
            String t = "";
            if (dr.isTiraLeite()) {
                t = "Sim";
            } else {
                t = "Não";
            }
            dtma.addRow(new Object[]{
                ID_pro,
                dr.getNomeFazenda(),
                dr.getAreaPropri(),
                dr.getMuniciSede(),
                de,
                dr.getLogradouro(),
                dr.getNIRF(),
                dr.getCodINCRA(),
                dr.getResidenciaAtual(),
                dr.getOutrasA(),
                t
            });
        });
        corLinhaJTable(false);
    }

    public Propriedades_Rurais preencher_objeto_Rural() {
        pr = new Propriedades_Rurais();

        if ("cadastrar".equals(status)) {
            pr.setId_proprie(0);
        } else if ("alterar".equals(status)) {
            if (clicoTabela_R) {
                pr.setId_proprie(id_propriedade_rural);
            } else {
                pr.setId_proprie(0);
            }
        }

        pr.setNomeFazenda(NOMEFAZENDA.getText());
        pr.setLogradouro(LOGRADOURO.getText());
        pr.setMuniciSede(MUNICEDE.getText());
        if ("   .   .   .   - ".equals(CODINCRA.getText())) {
            pr.setCodINCRA("");
        } else {
            pr.setCodINCRA(CODINCRA.getText());
        }
        if (" .   .   - ".equals(Numero_NIRF.getText())) {
            pr.setNIRF("");
        } else {
            pr.setNIRF(Numero_NIRF.getText());
        }
        String cr = AREAFAZENDA.getText();
        String cre = cr.replace(",", ".");
        pr.setAreaPropri(Double.parseDouble(cre));
        String dt = df.format(TEMPOCOMPRA.getDate());
        pr.setDataCompra(Util_Controller.STRING_DATE(dt));
        pr.setOutrasA(OUTRASATIVI.getText());
        pr.setResidenciaAtual(RESIDEN_ATUAL.getText());
        pr.setExcluido(false);

        if (TIRALEITE.isSelected()) {
            pr.setTiraLeite(true);
        } else {
            pr.setTiraLeite(false);
        }
        return pr;
    }

