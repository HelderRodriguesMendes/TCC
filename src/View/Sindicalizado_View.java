    private void TABELA_SINDMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TABELA_SINDMouseClicked
        Util_DAO ud = new Util_DAO();
        id_sindicalizado = Integer.parseInt(TABELA_SIND.getValueAt(TABELA_SIND.getSelectedRow(), 0).toString());
        if (null != status) {
            switch (status) {
                case "alterar": {
                    String ObjButtons[] = {"Alterar dados pessoais", "Alterar dados rurais"};
                    int escolha = JOptionPane.showOptionDialog(null,
                            "Escolha uma das opções abaixo", "ATENÇÃO",
                            JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                            ObjButtons, ObjButtons[1]);
                    if (escolha == 0) {
                        preencher_campus_pessoais(preencer_objeto_dadosP_tabela());
                        LISTAR_TABELA_Rural(DADOSR.listar_Tabela_RURAL(id_sindicalizado));
                        selecionar_guia(1);
                    } else if (escolha == 1) {
                        preencher_campus_pessoais(preencer_objeto_dadosP_tabela());
                        LISTAR_TABELA_Rural(DADOSR.listar_Tabela_RURAL(id_sindicalizado));
                        selecionar_guia(2);
                    }
                    break;
                }
                case "excluir": {
                    String ObjButtons[] = {"Sindicalizado", "Propriedade Rural"};
                    int escolha = JOptionPane.showOptionDialog(null,
                            "Escolha uma das opções de exclusão", "ATENÇÃO",
                            JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                            ObjButtons, ObjButtons[1]);
                    if (escolha == 0) {

                        boolean OK = AD.verificaAnuidadeNaoPaga_Sindicalizado(id_sindicalizado);
                        if (OK) {
                            String ObjButtons2[] = {"Sim", "Não"};
                            int escolha1 = JOptionPane.showOptionDialog(null,
                                    "Este sindicalizado possui taxas anuais pendentes, deseja excluí-las?", "ATENÇÃO",
                                    JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                                    ObjButtons2, ObjButtons2[1]);
                            if (escolha1 == 0) {
                                AD.excluirTaxasAnuais(id_sindicalizado);
                                ud.excluir_S_A(id_sindicalizado, "sind");
                                listar_Tabela_Sind();
                            } else if (escolha1 == 1) {
                                ud.excluir_S_A(id_sindicalizado, "sind");
                                listar_Tabela_Sind();
                            }
                        } else {
                            ud.excluir_S_A(id_sindicalizado, "sind");
                            AD.excluirTaxasAnuais(id_sindicalizado);
                            listar_Tabela_Sind();
                        }
                    } else if (escolha == 1) {
                        LISTAR_TABELA_Rural(DADOSR.listar_Tabela_RURAL(id_sindicalizado));
                        bloquia_todos_campus_R();
                        selecionar_guia(2);
                    }
                    break;
                }
                case "contrato": {
                    String ObjButtons[] = {"Sim", "Não"};
                    int escolha = JOptionPane.showOptionDialog(null,
                            "Deseja emitir relatório com os dados desse sindicalizado?", "ATENÇÃO",
                            JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null,
                            ObjButtons, ObjButtons[1]);
                    if (escolha == 0) {
                        Relatorios(id_sindicalizado);
                    }
                    break;
                }
                default:
                    break;
            }
        }
    }//GEN-LAST:event_TABELA_SINDMouseClicked

